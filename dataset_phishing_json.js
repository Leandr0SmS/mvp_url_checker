function hasHttpsToken(url) {
    const access_token = new URLSearchParams(url.search)//.get('access_token');
    console.log(access_token)
    return access_token;
}

const url_checker = (dataSet) => {
    const result = [];
    for (let data of dataSet) {
        const check = {};
        const urlTest = new URL(data.url)

        urlLenCount(urlTest) == data.length_url
            ? check.length_url = true
            : check.length_url = false
        
        urlLenHostname(urlTest) == data.length_hostname
            ? check.length_hostname = true
            : check.length_hostname = false

        hasHttpsToken(urlTest) == data.https_token
            ? check.https_token = true
            : check.https_token = false
        result.push(check)
    }
    return result
}

